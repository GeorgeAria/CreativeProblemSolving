#Most statements (logical lines) that you write will contain expressions.
#An expression has an operator and operands.
#An operator are functionality that do something to its operands
#Operands are the data that are operated on
print(2 + 3)

#In this example, * is the operator while 3 and 5 are the operands
print(3 * 5)

#Another example of expression
length = 5
breadth = 2

area = length * breadth
print('Area is', area)
print('Perimeter is', 2 * (length + breadth))
#Note that Python automatically puts a space after "is" for our output